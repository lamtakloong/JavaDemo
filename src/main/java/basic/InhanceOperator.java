package basic;

public class InhanceOperator {

  public static void main(String[] args) {
    int i=0;
    System.out.println(i+=8); //i = i+8
    System.out.println(i-=8); //i = i-8
    System.out.println(i*=8); //i = i*8
    System.out.println(i/=8); //i = i/8
    System.out.println(i%=8); //i = i%8

    i+=1; //statement
    System.out.println(i+=1);

  }

}
